package com.liu.pojo;

public class number {
    public  String number;

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    @Override
    public String toString() {
        return "number{" +
                "number='" + number + '\'' +
                '}';
    }
}
